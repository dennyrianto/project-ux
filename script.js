function menuKem() {
    var div = document.getElementById("menuNav2");
    if (div.style.display === "none") {
      div.style.display = "block";
      div.addEventListener("mouseleave", function() {
        div.style.display = "none";
      });
    } else {
      div.style.display = "none";
    }
  }

